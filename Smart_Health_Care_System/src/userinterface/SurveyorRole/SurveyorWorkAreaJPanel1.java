    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : surveyOrg.getWorkQueue().getWorkRequestList()){
            if(request instanceof AskFunds){
                if(!((AskFunds)request).isSurveyDone())
                { Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getSender().getPerson();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getPerson().getName();
            row[3] = request.getStatus();
            row[4]=((AskFunds)request).getRequestDate();
            row[5]=((AskFunds)request).getReason();
            model.addRow(row);}
        }}
    }

