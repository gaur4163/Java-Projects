    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        try{
        if((resultjTextArea1.getText()).length()==0)
           { 
               JOptionPane.showMessageDialog(this, "Please fill the Result Field","ERROR", JOptionPane.ERROR_MESSAGE);
           }
        else{
    
        request.setResult(resultjTextArea1.getText());
        request.setResolveDate(new Date());
        request.setStatus("Completed");
        populateTable();
        JOptionPane.showMessageDialog(this, "The Request has been processed", "SUCCESS", JOptionPane.PLAIN_MESSAGE);
        submitJButton.setEnabled(false);
        clearFields();
      }
        }catch (Exception e){
                 JOptionPane.showMessageDialog(this, "No Data Found","ERROR",JOptionPane.ERROR_MESSAGE);
                 return; 
        }
    }//GEN-LAST:event_submitJButtonActionPerformed
